import UIKit


extension UIColor {

	class func backgroundBlack() -> UIColor {
		return UIColor(red: 40/255, green: 42/255, blue: 55/255, alpha: 1.0)
	}

	class func stringRed() -> UIColor {
		return UIColor(red: 220/255, green: 57/255, blue: 59/255, alpha: 1.0)
	}

	class func numberPurple() -> UIColor {
		return UIColor(red: 134/255, green: 130/255, blue: 188/255, alpha: 1.0)
	}

	class func keywordPurple() -> UIColor {
		return UIColor(red: 180/255, green: 57/255, blue: 147/255, alpha: 1.0)
	}

	class func statementYellow() -> UIColor {
		return UIColor(red: 210/255, green: 140/255, blue: 92/255, alpha: 1.0)
	}

	class func buildInBlue() -> UIColor {
		return UIColor(red: 1/255, green: 169/255, blue: 162/255, alpha: 1.0)
	}

	class func lightGreen() -> UIColor {
		return UIColor(red: 146/255, green: 196/255, blue: 109/255, alpha: 1.0)
	}

	class func commentGreen() -> UIColor {
		return UIColor(red: 85/255, green: 183/255, blue: 86/255, alpha: 1.0)
	}

	class func plainWhite() -> UIColor {
		return UIColor.whiteColor()
	}


}
